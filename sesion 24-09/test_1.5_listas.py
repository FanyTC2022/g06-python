"""Tipos de datos basicos"""

""" tipo de datos: listas"""

lista = [20, "luis", "Calle de alamedas", 1314]

print("El valor de mi variable lista es: {}". format(lista))

"""Tipo de dato: Tupla"""

tupla = (40, 50.5, 30, 100)

print("El valor de mi variable tupla es {}".format(tupla))

"""Tipo de dato: Set"""

conjunto = set(["hola", 40, 3, 45, 40, 11, 3, 10])

print("El valor de mi variable conjunto es: {}". format(conjunto))

diccionario = {"nombre": "Juan", "apellido": "taquila", "edad": 25, "dni": 76273198}

print("El valor de mi variable diccionario es: {}".format(diccionario))

