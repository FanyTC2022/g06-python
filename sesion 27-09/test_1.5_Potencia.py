""" Operaciones aritméticas con Python """

""" Operador potencia: ** """

potencia1 = 4**3

print("\nEl resultado de mi potencia 1 es: {}".format(potencia1))


""" Usando la palabra reservada 'pow' podemos tambien 
    elevar a una cierta potencia un número"""

"""Nos pedira 2 valores, el primero es la base y el segundo es la potencia"""

potencia2 = pow(20,4)
potencia3 = pow(5,4)

print("El resultado de mi potencia 2 es: {}".format(potencia2))
print("El resultado de mi potencia 3 es: {}".format(potencia3))